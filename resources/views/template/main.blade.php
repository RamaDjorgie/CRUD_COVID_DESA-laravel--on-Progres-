        $(document).ready(function() {
  /**
   * for showing edit item popup
   */

  $(document).on('click', "#edit-item", function() {
    $(this).addClass('edit-item-trigger-clicked'); //useful for identifying which trigger was clicked and consequently grab data from the correct row and not the wrong one.

    var options = {
      'backdrop': 'static'
    };
    $('#edit-modal').modal(options)
  })

  // on modal show
  $('#edit-modal').on('show.bs.modal', function() {
    var el = $(".edit-item-trigger-clicked"); // See how its usefull right here?
    var row = el.closest(".data-row");

  // get the data
  var id = el.data('item-id');
    var nama_pasien = row.children(".nama_pasien").text();
    var jenis_kelamin = row.children(".jenis_kelamin").text();
    var asal_daerah = row.children(".asal_daerah").text();
    var no_pelapor = row.children(".no_pelapor").text();
    var status = row.children(".status").text();

    // fill the data in the input fields
    $("#modal-input-id").val(id);
    $("#modal-input-nama-pasien").val(nama_pasien);
    $("#modal-input-jenis-kelamin").val(jenis_kelamin);
    $("#modal-input-asal-daerah").val(asal_daerah);
    $("#modal-input-no-pelapor").val(no_pelapor);
    $("#modal-input-status").val(status);


  })

  // on modal hide
  $('#edit-modal').on('hide.bs.modal', function() {
    $('.edit-item-trigger-clicked').removeClass('edit-item-trigger-clicked')
    $("#edit-form").trigger("reset");
  })
})
